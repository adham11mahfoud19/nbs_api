      <div  class="content" id="article5">
        <h1 class="title">Article 5: PILLARS</h1>
        <h2>SEVEN PILLARS OF HIGH ACHIEVEMENT</h2>
        <p>NBS’ Seven pillars of high achievement are the philosophical and practical framework for the activities of this society in each geographical region.</p>
        
        <ol>
          <li>NBS Service, the first pillar of high achievement, involves action a member should take within this society to help it function successfully. </li>
          
          <li>Community Service, the second pillar of high achievement, to improve the quality of life of those who live within his community. </li>
         
          <li>Global Service, the third pillar of high achievement, comprises those activities that members do to advance international understanding, goodwill, and peace by creating a culture of respect and cooperation with people of other countries. </li>
         
          <li>Cultural Understanding, the fourth pillar of high achievement, to engender understanding towards other cultures, customs, accomplishments, aspirations, and problems. This can be done through reading and correspondence with cooperation in society activities and projects designed to help people. </li>
          
          <li>Professional and Vocational Service, the fifth pillar of high achievement, has the purpose of promoting high ethical standards in businesses and professions. </li>
         
          <li>Honest Business, the sixth pillar of high achievement, recognizing the worthiness of all dignified occupations, and producing the ideal of achievement in the pursuit of all vocations. The role of members includes conducting themselves and their businesses in accordance with NBS’ principles. </li>
         
          <li>New Generations Service, the seventh pillar of high achievement, recognizes the positive change implemented by youth and young adults through leadership development activities, involvement in community and international service projects, and exchange programs that enrich and foster world peace and cultural understanding.</li>     
        </ol>

      </div><!-- End post content -->

      <div  class="content" id="article6">
        <h1 class="title">Article 6: Governance Roles</h1>
        <h2>The Commission of The Board, Ambassadors, Officers and Members</h2>
        <h3>Section 1: The Board</h3>
        <p>The Commission of the founders (the Board) of the “NBS” composed of the founders who are responsible for the decisions that make up:</p>
        
        <ol type="a">
          <li>Tactical strategy, bylaws and as well as amendments to be made. </li>
          
          <li>The final decision in urgent management conflicts, or the international relations. </li>
         
          <li>Appointing the “Ambassadors”. </li>
         
          <li>Overseeing the performance of the commissions members as well as the performances of the honorary members. Supervising of the officers’ Elections, terminations, freezing and the retiring of members.</li>    
        </ol>

        <h3>Section 2: The Ambassador</h3>
        <p>The Board shall appoint one Ambassador for each region. He shall supervise the following tasks: </p>

        <ol type="a">
          <li>The implementation of all tasks and plans for his region according to the strategy which are put up by the commission of the founders. </li>
          
          <li>Recommending persons wishing to he wants to nominate to permanent membership or honorary members. </li>
         
          <li>Send a request for membership approval to the Commission of the founders.</li>
         
          <li>The distribution of members on committees according to their qualifications and supervision of the implementation of the tasks assigned to them. </li>    
        
          <li>Providing the Committee of the founders with detailed reports on the activities and achievements of the commissions as well as the progress of implementation at each stage of the work in their region. </li>
       
          <li>Conducting the election of the officers of each Committee every two years or when necessary which may be supervised by one or more founding member to ensure there is proper application of the bylaws. </li>
       
          <li>An annual meeting for all the society officers shall be held not later than 31 December as provided in the bylaws or when urgency necessitates it. </li>
       
          <li>Any regular meeting or election, a minimum must be present, by the affirmative vote of not less than two-thirds of all voting members present.</li>
        </ol>

        <h3>Section 3: The officers</h3>

        <ol type="a">
          <li>Elections: Each officer shall be elected as provided in the bylaws. Except for the ambassador, each officer shall take office immediately following election and shall serve for the term of office or until a successor has been duly elected and qualified. </li>
          
          <li>Qualifications: Each officer shall be a member in good standing of this society. He shall attend the district officer-elect training seminar and the district assemblies unless excused by the Ambassador. </li>
         
          <li>Meetings -Day and Time-: Each officer shall hold a regular meeting once each month on the day or when needed; meetings with his members may be either in person or by posting an interactively online on the day they cooperatively appointed. </li>
         
          <li>Change of Meeting: For good cause, the officer may change a regular meeting to any day during the month. </li>    
        
          <li>Cancellation: The officer may cancel a regular meeting if it falls on a legal holiday, including a commonly recognized holiday, or in case of the death of a society member, or of a state of emergency. The officer may cancel not more than four regular meetings in a year for causes not otherwise specified herein provided that this society does not fail to meet for more than three consecutive meetings.</li>  
       </ol>

       <h3>Section 4: Members</h3>
       <p>General Qualifications: The society shall be composed of adult persons (female and male) of good character and good business, professional and/or community reputation. This society shall have two kinds of membership, namely: active and honorary. The member must possess the qualifications that are with the “Seven Pillars of High Achievement” (article 5). The members should be approved by the Founders (The Board) of NBS.</p>

      </div><!-- End post content -->

