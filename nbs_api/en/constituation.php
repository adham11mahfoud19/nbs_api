        <h1 class="title">Article 4: OBJECT</h1>
        <p>The object of “NBS” is to develop countries stability and, in particular, to promote and support: </p>
        
        <ol>
          <li>Enabling communities which refers to the process of empowering them to increase control over their lives and 'Empowerment' refers to the process by which people gain control over the factors and decisions that shape their lives; </li>
          
          <li>Literacy and prosperous behavior; </li>
         
          <li>The development of opportunities for work; </li>
         
          <li>High ethical standards in enterprise and trades; </li>
          
          <li>The recognition of all skilled occupations; </li>
         
          <li>The constant improvement of international understanding towards peace through a multinational brotherhood to be united in the ideal of democracy;</li>
         
          <li>The distinguished NBS’ members and their roles in part to serve society; </li>
          
          <li>The recognized effort in each NBS member’s personal life, business life, and community.</li>
         
          <li>Treat issues of poverty effectively with the community as a coalition</li>
         
        </ol>

