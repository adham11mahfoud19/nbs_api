      <div  class="content" id="article13">
        <h1 class="title">Article 13: Duration of Membership </h1>
        <h3>Section 1: Period</h3>
        <p>Membership shall continue during the existence of this society unless terminated as hereinafter provided. </p>
        <h3>Section 2: Automatic Termination</h3>
        <ol type="a">
          <li>Membership Qualifications. Membership shall automatically be terminated when a member no longer meets the membership qualifications. Exception being that a member is moving. His membership will be recognized once he is well known to the new community as long as the member continues to meet all conditions of local membership. </li>
          <li>How to Rejoin: When the membership of a member has terminated as provided in subsection (a) of this section, such person, provided such person’s membership was in good standing at the time of termination, and may make new application for membership, under the same or another rank. A second admission fee shall not be required unless noted in a termination. </li>
          <li>Termination of Honorary Membership: Honorary membership shall automatically terminate at the end of the term for such membership as determined by the board. However, the Board may extend an honorary membership for an additional period. The board may revoke an honorary membership at any time.</li>
        </ol>
        <h3>Section 3: Termination — Non-payment of Dues</h3>
        <ol type="a">
          <li>Process: Any member failing to pay dues within thirty (30) days after the prescribed time shall be notified in writing by the Ambassador at the member’s last known address. If the dues are not paid on or before ten (10) days of the date of notification, membership may terminate, subject to the discretion of the Board.</li>
          <li>Reinstatement: The board may reinstate the former member to membership upon the former member’s petition and payment of all indebtedness to this society. However, no former member may be reinstated to active membership if the former member’s classification is in conflict with article 8, section </li>
        </ol>
        <h3>Section 4: Termination — Non-attendance</h3>
        <ol >
          <li>
              Attendance Percentages. A member must: 
            <ol type="a">
                <li>Attend or make up at least 50 percent of regional regular meetings in each half of the year;</li>
                <li>Attend at least 30 percent of this regional regular meetings in each half of the year. If a member fails to attend as required, the member’s membership shall be subject to termination unless the board consents to such non- attendance for good cause. </li>
            </ol>
          </li>
          <li>
            <p>Consecutive Absences: </p>
            <p>Unless otherwise excused by the Board for good and sufficient reason or pursuant to the bylaws, each member who fails to attend or make up four consecutive regular meetings shall be informed by the board that the member’s non-attendance may be considered a request to terminate membership in this society. Thereafter, the Board, by a majority vote, may terminate the member’s membership.</p>
          </li>
        </ol>
          <h3>Section 5: Termination — Other Causes: </h3>
          <ol type="a">

            <li>Good Cause. The board may terminate the membership of any member who ceases to have the qualifications for membership in this society or for any good cause by a vote of not less than two-thirds of the regional members, at a meeting called for that purpose. The guiding principles for this meeting shall be article 8, section 1; The Four-Way Test; and the high ethical standards that one should hold as a NBS member. </li>

            <li>Notice. Prior to taking any action under subsection (a) of this section, the member shall be given at least ten (10) days’ written notice of such pending action and an opportunity to submit a written answer to the board. The member shall have the right to appear before the Board to state the member’s case. Notice shall be by personal delivery or by registered letter to the member’s last known address. </li>

            <li>Filling Classification. When the board has terminated the membership of a member as provided for in this section, this society shall not elect a new member under the former member’s classification until the time for hearing any appeal has expired and the decision of this society or of the arbitrators has been announced. However, this provision shall not apply if, by election of a new member, the number of members under the said classification would remain within provided limitations even if the Board’s decision regarding termination is reversed.</li>

          </ol>
          <h3>Section 6:  Right to Appeal, Mediate or Arbitrate Termination</h3>
          <ol type="a">

            <li>Notice. Within seven (7) days after the date of the board’s decision to terminate membership, the secretary shall give written notice of the decision to the member. Within fourteen (14) days after the date of the notice, the member may give written notice to appeal to the society, request mediation, or to arbitrate as provided in the bylaws.</li>

            <li>
              <p>Date for Hearing of Appeal.</p>
              <p>In the event of an appeal, the Board shall set a date for the hearing of the appeal at a regular society meeting to be held within twenty-one (21) days after letter of the notice for appeal. At least five (5) days’ written notice of the meeting and its special business shall be given to every member. Only members shall be present when the appeal is heard.</p>
            </li>

            <li>
              <p>Mediation or Arbitration. </p>
              <p>The procedure utilized for mediation or arbitration shall be as provided in the bylaws. </p>
            </li>

            <li>
              <p>Appeal. </p>
              <p>If an appeal is taken, the action of the society shall be final and binding on all parties and shall not be subject to arbitration. </p>
            </li>

            <li>
              <p>Decision of Arbitrators or Umpire. </p>
              <p>If arbitration is requested, the decision reached by the arbitrators or, if they disagree, by the appointed council shall be final and binding on all parties and shall not be subject to appeal. </p>
            </li>

            <li>
              <p>Unsuccessful Mediation. </p>
              <p>If mediation is requested but is unsuccessful, the member may appeal to the society or arbitrate as provided in subsection (a) of this section.</p>
            </li>

         
          </ol>
