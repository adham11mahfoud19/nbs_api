        <h1 class="title">Article 7: Classifications</h1>
        <h3>Section 1: General Provisions: </h3>
        <p>Principal Activity: </p>
        <p>Each member shall have a rank of position in accordance with the member’s specialization, education or type of community service. The hierarchy of members describes the principal and recognized activity of which the member is connected or that which describes the member’s recognized profession. Or that which describes the nature of the member’s role in his community. </p>
        <p>Correction or Adjustment: </p>
        <p>If the circumstances warrant, the Board may correct or adjust the classification of any member. Notice of a proposed correction or adjustment shall be provided to the member and the member shall be allowed a hearing thereon.</p>
        
        <h3>Section 2: Limitations: </h3>
        <p>The Board shall not permit a person to a rank if the region’s members already has fifteen (15) or more people in that rank, exception is if there are more than hundred (100) members, in which case, the Board may elect a person to active membership in a classification so long as it will not result in the classification making up more than twenty percent (20%) of the society’s active membership. Members who are retired or froze their activities shall not be included in the total number of members in a classification. </p>
        <p>If a member changes rank, the Board may continue the member’s membership under the new rank unabiding these limitations.</p>
      
