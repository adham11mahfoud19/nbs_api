      <div  class="content" id="article8">
        <h1 class="title">Article 8: Attendance </h1>
        <h3>Section 1: General Provisions</h3>
        <p>Each member must attend the regional regular meetings in person or posted online. A member shall be counted as attending a regular meeting if the member is present for at least 70 percent of the meeting, or is present and is called away unexpectedly and subsequently produces evidence to the satisfaction of the Board that such action was reasonable, or makes up for an absence in any appropriate manner which must be accepted by the Board according to the following issues:</p>
        
        <ol type="a">
          <li>For reasons which it considers to be good and sufficient. Such excused absences shall not extend for longer than twelve months. </li>
          <li>If the member notified the society secretary in writing of the member’s desire to be excused from attendance and the Board has approved. </li>
          <li>If, at the time of the meeting, the member is traveling with reasonable directness to or from one of the meetings specified in society missions directly and actively engaged in society business duly authorized by the Board which precludes attendance at the meeting. </li>
          <li>A member’s absence shall be excused if the absence complies with the conditions and under circumstances approved by the board.</li>
        </ol>

      </div><!-- End post content -->

      <div  class="content" id="article12">
        <h1 class="title">Article 12: Admission Fees and Dues </h1>
        <p>Every member shall pay an admission fee and annual dues as prescribed in the bylaws, which will be formulated by the Board and Ambassadors if necessity required that.</p>

