                <div class="content">
                  <p>
                      At the Nation Builders Society (NBS), we operate under a robust governance structure that ensures effective management, transparency, and fair decision-making. Our leadership framework consists of The Board, Ambassadors, Executive Officers, and Members, each playing a critical role in achieving the Society’s objectives and supporting the communities we serve.                 </p>
                  <p>
                  <blockquote>
                    <p>
                    The Society sheds light on human rights violations, drawing global attention to such cases

                    </p>
                  </blockquote>



                  
               
                  <img src="assets/img/board2.jpg" class="img-fluid mb-2" alt="">
                  <strong class="my-2">
                  The Board (Executive Commission)
                  </strong>
            <p>
            The Executive Board, or the Founders’ Commission, serves as the highest governing body within NBS and is responsible for:
            </p>
            <ul>
              <li> <span>Developing strategic policies that define the Society’s direction.</span></li>
              <li> <span>Reviewing and amending bylaws to maintain operational efficiency.</span></li>
              <li> <span>Making final decisions on urgent administrative conflicts and international affairs.</span></li>
              <li> <span>Appointing Ambassadors to oversee the execution of plans in different regions.</span></li>
              <li> <span>Supervising committee performance and ensuring compliance with NBS standards.</span></li>
              <li> <span>Overseeing elections, appointments, and membership status adjustments.</span></li>
            </ul>
            <p>
            The Board ensures that all decisions align with NBS’s core principles, promoting integrity, inclusivity, and ethical governance.
            </p>

            <strong>
            Ambassadors
            </strong>

            <p>
            An Ambassador is appointed for each geographical region, responsible for:
                        </p>
            <ul>
              <li> <span>Implementing NBS’s policies and strategies at the regional level.</span></li>
              <li> <span>Recommending candidates for permanent or honorary membership and coordinating their approval.</span></li>
              <li> <span>Providing comprehensive reports on activities and progress to ensure alignment with strategic objectives.</span></li>
              <li> <span>Organizing regional meetings and overseeing committee elections every two years or when necessary.</span></li>
              <li> <span>Representing NBS regionally, liaising with partners, stakeholders, and international bodies.</span></li>

            </ul>
            <p>
            Ambassadors serve as the vital link between the Executive Board and regional members, ensuring smooth coordination and alignment with NBS’s mission.            </p>
            <strong>
            Executive Officers
            </strong>
            <ul>
              <li> <span>Elected based on NBS bylaws, Executive Officers take office immediately upon election.</span></li>
              <li> <span>Their responsibilities include overseeing daily operations, implementing policies, and managing meetings.</span></li>
              <li> <span>Officers must attend governance training and regular meetings to maintain high leadership standards.</span></li>
            </ul>

                  <blockquote>
                    <p>
                    The Society actively engages in drafting legal agreements with international organisations that share its mission and aspirations

                </blockquote>
            
                  <strong>
                  Meetings & Administration
            </strong>
            <ul>
              <li> Monthly meetings are held either in person or online to coordinate activities.</li>
              <li>Officers may reschedule meetings for valid reasons, but no more than four meetings per year may be canceled.></li>
            </ul>
            <strong>
            Members
            </strong>
            <p>
            Members form the foundation of NBS and are classified into two categories:
           </p>
            <ul>
              <li>
               <span>
               Active Membership
                <ul>
                    <li>Open to individuals with strong professional and community reputations.</li>
                    <li>Members must attend regular meetings and actively participate in NBS initiatives.</li>
                    <li>Members are classified based on their expertise and contributions.</li>
                   </ul> 
              </span>
            </li>
              <li>
               <span>
               Honorary Membership
                   <ul>
                    <li>Granted to individuals in recognition of outstanding contributions to society.</li>
                    <li>Approved by the Board and subject to renewal or termination as needed.</li>
                   </ul> 
              </span>
            </li>
            
          </ul>
          <p>
          Members undergo periodic assessments, and the Board reserves the right to reclassify or adjust their membership as required.
          </p>
          <strong>
          Meetings & Elections
          </strong>
          <ul>
              <li>An annual meeting for all NBS officers is held no later than 31 December.</li>
              <li>Regional meetings take place monthly to ensure coordination and strategy implementation.</li>
              <li>Executive elections occur every two years, with a Founding Member supervising the process to ensure fairness.</li>
          </ul>
          <p>
          Decisions require a majority vote and must be approved by at least two-thirds of voting members present.
         </p>
          <strong>
          Transparency & Accountability
          </strong>
          <ul>
              <li>NBS upholds the highest standards of governance, ensuring transparency in all decisions and processes.</li>
              <li> Members have the right to request clarifications, appeal decisions, and engage in Society discussions.</li>
              <li>Meeting minutes and official decisions are made accessible to members to maintain full transparency.</li>
         </ul>
