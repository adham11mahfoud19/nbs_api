        <h1 class="title">المادة 6: أدوار الحوكمة</h1>
        <h2>لجنة المجلس، السفراء، المسؤولون التنفيذيون، والأعضاء</h2>
        <h3>القسم 1: المجلس التنفيذي (مجلس المؤسسين)</h3>
        <p>يتألف مجلس المؤسسين (The Board) في رابطة بناة الأمة من المؤسسين الذين يتحملون المسؤوليات التالية:</p>
        
        <ol>
          <li>وضع الاستراتيجيات التكتيكية، واعتماد الأنظمة الأساسية، وإدخال التعديلات عند الحاجة.</li>
          <li>اتخاذ القرار النهائي في النزاعات الإدارية العاجلة أو في الشؤون الدولية.</li>
          <li>تعيين "السفراء" للإشراف على المناطق المختلفة.</li>
          <li>متابعة أداء أعضاء اللجان وأداء الأعضاء الفخريين.</li>
          <li>الإشراف على الانتخابات، وإنهاء العضوية، وتجميدها، والتقاعد للأعضاء وفقًا للوائح المعتمدة.</li>
        </ol>

        <h3>القسم 2: السفير</h3>
        <p>يقوم المجلس التنفيذي بتعيين سفير واحد لكل منطقة، ويتولى المسؤوليات التالية:</p>

        <ol>
          <li>تنفيذ جميع الخطط والمهام في منطقته وفقًا للاستراتيجية التي يضعها مجلس المؤسسين.</li>
          <li>ترشيح الأفراد الراغبين في الحصول على العضوية الدائمة أو العضوية الفخرية.</li>
          <li>تقديم طلبات اعتماد العضوية إلى مجلس المؤسسين.</li>
          <li>توزيع الأعضاء على اللجان وفقًا لمؤهلاتهم، والإشراف على تنفيذ المهام الموكلة إليهم.</li> 
          <li>تقديم تقارير مفصلة إلى لجنة المؤسسين حول الأنشطة والإنجازات، وكذلك التقدم المحرز في كل مرحلة من مراحل العمل في منطقته.</li>        
          <li>الإشراف على انتخابات المسؤولين التنفيذيين لكل لجنة كل سنتين، أو عند الضرورة، مع إمكانية الإشراف من قبل عضو أو أكثر من المؤسسين لضمان التطبيق السليم للأنظمة الأساسية.</li>       
          <li>عقد اجتماع سنوي لجميع مسؤولي الرابطة في موعد لا يتجاوز 31 ديسمبر، وفقًا للوائح، أو عند الضرورة العاجلة.</li>       
          <li>في أي اجتماع دوري أو انتخابات، يجب أن يكون الحد الأدنى للحضور متحققًا، ويتم اتخاذ القرارات بأغلبية لا تقل عن ثلثي الأعضاء المصوتين الحاضرين.</li>       
        </ol>
        <h3>القسم 3: المسؤولون التنفيذيون</h3>
        <ol>
          <li>
            <strong>الانتخابات:</strong> يتم انتخاب كل ضابط وفقًا للوائح، ويبدأ في أداء مهامه فور انتخابه، ويبقى في منصبه طوال فترة ولايته أو حتى يتم انتخاب خلف له. ويستثنى من ذلك السفير، حيث يتم تعيينه من قبل المجلس.
          </li>
          <li>
            <strong>المؤهلات:</strong> يجب أن يكون كل ضابط عضوًا نشطًا في الرابطة، ويتمتع بسجل جيد، ويكون ملزمًا بحضور الدورات التدريبية للمسؤولين التنفيذيين المنتخبين والاجتماعات الإقليمية، ما لم يتم إعفاؤه من قبل السفير.
          </li>
          <li>
            <strong>الاجتماعات (اليوم والتوقيت):</strong> يعقد كل ضابط اجتماعًا منتظمًا مرة واحدة شهريًا، أو عند الضرورة، ويمكن أن تُعقد الاجتماعات حضوريًا أو عبر الإنترنت في اليوم المتفق عليه بشكل مشترك مع الأعضاء.
          </li>
          <li>
            <strong>تغيير موعد الاجتماع:</strong> يجوز للضابط، لأسباب مبررة، تغيير موعد الاجتماع الدوري ليعقد في أي يوم آخر خلال نفس الشهر.
          </li>
          <li>
            <strong>إلغاء الاجتماعات:</strong>
            <ol type="a">
              <li>يجوز للضابط إلغاء اجتماع دوري إذا صادف يوم عطلة رسمية، أو وفاة أحد أعضاء الرابطة، أو حالة طوارئ.</li>
              <li>لا يجوز إلغاء أكثر من أربعة اجتماعات منتظمة سنويًا، إلا إذا كان السبب منصوصًا عليه في اللوائح.</li>
              <li>لا يجوز أن تتوقف الرابطة عن عقد الاجتماعات لأكثر من ثلاثة اجتماعات متتالية.</li>
            </ol>
          </li>
        </ol>

        <h3>القسم 4: الأعضاء</h3>
        <ol>
          <li>
            <strong>المؤهلات العامة:</strong>
            <ol type="a">
              <li>تتكون الرابطة من أشخاص بالغين (ذكور وإناث) ذوي أخلاق حسنة وسمعة طيبة في مجالات الأعمال أو المهن أو المجتمع.</li>
              <li>تنقسم العضوية إلى نوعين:
                <ol type="i">
                  <li>عضوية نشطة (Active Membership).</li>
                  <li>عضوية فخرية (Honorary Membership).</li>
                </ol>
              </li>
              <li>يجب أن يمتلك العضو المؤهلات التي تتوافق مع "الركائز السبع للإنجاز العالي" (المادة 5).</li>
              <li>يجب أن يحصل جميع الأعضاء على موافقة المؤسسين (المجلس التنفيذي) للرابطة لقبول عضويتهم.</li>
            </ol>
          </li>
        </ol>
