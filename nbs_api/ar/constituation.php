        <h1 class="title">المادة 7: التصنيفات</h1>
        <h3>القسم 1: الأحكام العامة</h3>
        <p><strong>النشاط الرئيسي:</strong></p>
        <p>يتمتع كل عضو بمرتبة أو تصنيف وفقًا لتخصصه أو تعليمه أو نوع الخدمة المجتمعية التي يقدمها. تعكس هيكلية الأعضاء النشاط الأساسي والمعترف به الذي يرتبط به العضو، سواء كان ذلك مهنته الرسمية أو الدور الذي يؤديه في مجتمعه.</p>
        <p><strong>التصحيح أو التعديل:</strong></p>
        <p>إذا اقتضت الظروف، يحق للمجلس التنفيذي تصحيح أو تعديل تصنيف أي عضو. وفي هذه الحالة، يتم إخطار العضو بالتعديل المقترح، ومنحه فرصة للاستماع إليه قبل تنفيذ أي تغيير.</p>

        <h3>القسم 2: القيود</h3>
        <ol>
          <li>لا يجوز للمجلس التنفيذي منح مرتبة معينة لأي عضو جديد إذا كان عدد الأعضاء المصنفين في تلك المرتبة في المنطقة 15 عضوًا أو أكثر، إلا في الحالات التي يكون فيها العدد الإجمالي للأعضاء أكثر من 100 عضو، عندها يمكن للمجلس السماح بمنح العضوية في التصنيف المطلوب ما لم يتجاوز هذا التصنيف نسبة 20٪ من إجمالي الأعضاء النشطين في الرابطة.</li>
          <li>لا يتم احتساب الأعضاء المتقاعدين أو الذين قاموا بتجميد عضويتهم ضمن العدد الإجمالي للأعضاء في تصنيف معين.</li>
          <li>في حال قام أحد الأعضاء بتغيير تصنيفه أو رتبته، يحق للمجلس استمرار عضويته في التصنيف الجديد دون التقيد بهذه القيود.</li>
        </ol>

        <h3>الارتباط بالركائز السبع للإنجاز العالي</h3>
        <p>يتماشى تصنيف الأعضاء مع الركائز السبع للإنجاز العالي المعتمدة في الرابطة، وتشمل:</p>
        <ol>
          <li><strong>خدمة الرابطة (NBS Service):</strong> تنفيذ الأنشطة التي تضمن نجاح واستمرارية عمل الرابطة بفعالية.</li>
          <li><strong>خدمة المجتمع (Community Service):</strong> العمل على تحسين جودة الحياة داخل المجتمع المحلي.</li>
          <li><strong>الخدمة العالمية (Global Service):</strong> تعزيز التفاهم الدولي والتعاون والسلام من خلال تعزيز ثقافة الاحترام والتواصل بين الشعوب.</li>
          <li><strong>التفاهم الثقافي (Cultural Understanding):</strong> تشجيع التفاعل الثقافي من خلال القراءة والتواصل والتعاون في الأنشطة والمشاريع المجتمعية.</li>
          <li><strong>الخدمة المهنية والوظيفية (Professional and Vocational Service):</strong> تعزيز المعايير الأخلاقية العالية في بيئات العمل والمهن المختلفة.</li>
          <li><strong>الأعمال النزيهة (Honest Business):</strong> ترسيخ مبدأ العمل النزيه واحترام جميع المهن المشروعة، والسعي لتحقيق التميز في كافة المجالات.</li>
          <li><strong>خدمة الأجيال الجديدة (New Generations Service):</strong> تمكين الشباب من إحداث تغيير إيجابي عبر تنمية مهاراتهم القيادية، والمشاركة في المشاريع المجتمعية والدولية، وبرامج التبادل التي تعزز السلام والتفاهم الثقافي.</li>
        </ol>
      </div><!-- End post content -->

      <div class="content" id="article8">
        <h1 class="title">المادة 8: الحضور</h1>
        <h3>القسم 1: الأحكام العامة</h3>
        <ol>
          <li>يجب على كل عضو حضور الاجتماعات الدورية الإقليمية شخصيًا أو عبر الإنترنت.</li>
          <li>يعتبر العضو حاضرًا رسميًا إذا شارك في ما لا يقل عن 70٪ من مدة الاجتماع، أو إذا كان حاضرًا ثم اضطر إلى المغادرة فجأة، وقام بتقديم دليل مقنع للمجلس التنفيذي يثبت أن غيابه كان مبررًا ومعقولًا.</li>
          <li>يجوز للعضو تعويض غيابه بوسيلة مناسبة، بشرط أن يوافق عليها المجلس التنفيذي وفقًا للضوابط التالية:
            <ol type="a">
              <li>إذا رأى المجلس أن الغياب كان لأسباب وجيهة وكافية، على ألا تتجاوز فترة الغياب المعفى منها اثني عشر شهرًا.</li>
              <li>إذا قام العضو بإبلاغ أمين سر الرابطة كتابيًا برغبته في الإعفاء من الحضور، وحصل على موافقة المجلس التنفيذي.</li>
              <li>إذا كان العضو، في وقت الاجتماع، مسافرًا مباشرة لحضور اجتماع رسمي آخر للرابطة، أو كان مشغولًا بأعمال رسمية موكلة إليه من قبل المجلس التنفيذي، مما جعله غير قادر على الحضور.</li>
              <li>يتم إعفاء غياب العضو إذا كان يتوافق مع الشروط والظروف المعتمدة من قبل المجلس التنفيذي.</li>
            </ol>
          </li>
        </ol>
      </div><!-- End post content -->

      <div class="content" id="article12">
        <h1 class="title">المادة 12: رسوم القبول والاشتراكات</h1>
        <p>يتعين على كل عضو دفع رسوم القبول والاشتراكات السنوية وفقًا لما هو منصوص عليه في النظام الأساسي، ويتم تحديد هذه الرسوم من قبل المجلس التنفيذي والسفراء إذا دعت الحاجة لذلك.</p>
      </div><!-- End post content -->

      <div class="content" id="article13">
        <h1 class="title">المادة 13: مدة العضوية</h1>
        <h3>القسم 1: الفترة</h3>
        <p>تستمر العضوية طوال فترة وجود هذه الرابطة، ما لم يتم إنهاؤها وفقًا لما هو منصوص عليه أدناه.</p>

        <h3>القسم 2: الإنهاء التلقائي</h3>
        <ol>
          <li>
            <strong>متطلبات العضوية:</strong> يتم إنهاء العضوية تلقائيًا إذا لم يعد العضو مستوفيًا لشروط العضوية.
            <ol type="a">
              <li>الاستثناء الوحيد: في حال انتقال العضو إلى منطقة أخرى، يتم الاعتراف بعضويته فور اندماجه في المجتمع الجديد، شريطة استمراره في استيفاء جميع متطلبات العضوية المحلية.</li>
            </ol>
          </li>
          <li>
            <strong>إعادة الانضمام:</strong>
            <ol type="a">
              <li>يجوز لأي شخص انتهت عضويته وفقًا لهذه المادة التقدم بطلب جديد للعضوية، سواء في نفس التصنيف أو في تصنيف آخر، شريطة أن يكون سجله سليماً عند إنهاء العضوية.</li>
              <li>لا يُطلب منه دفع رسوم قبول ثانية، ما لم يكن الإنهاء ناتجًا عن عدم السداد أو أسباب تأديبية.</li>
            </ol>
          </li>
          <li>
            <strong>إنهاء العضوية الفخرية:</strong>
            <ol type="a">
              <li>يتم إنهاء العضوية الفخرية تلقائيًا بنهاية المدة المحددة لها وفقًا لما يقرره المجلس التنفيذي.</li>
              <li>يجوز للمجلس تمديد العضوية الفخرية لفترة إضافية.</li>
              <li>يحق للمجلس إلغاء العضوية الفخرية في أي وقت.</li>
            </ol>
          </li>
        </ol>

        <h3>القسم 3: الإنهاء بسبب عدم دفع الاشتراكات</h3>
        <ol>
          <li>
            <strong>الإجراءات:</strong>
            <ol type="a">
              <li>يتم إخطار أي عضو لم يقم بسداد الاشتراكات خلال 30 يومًا من تاريخ الاستحقاق كتابيًا عبر السفير الإقليمي وإرساله إلى آخر عنوان معروف له.</li>
              <li>في حالة عدم السداد خلال 10 أيام من تاريخ الإخطار، يمكن إنهاء العضوية وفقًا لتقدير المجلس التنفيذي.</li>
            </ol>
          </li>
          <li>
            <strong>إعادة العضوية:</strong>
            <ol type="a">
              <li>يجوز للمجلس إعادة العضو السابق إلى الرابطة بعد تقديم طلب رسمي وسداد جميع المستحقات.</li>
              <li>لا يمكن إعادة العضوية إذا كان التصنيف الجديد يتعارض مع القيود المنصوص عليها في المادة 8، القسم 1.</li>
            </ol>
          </li>
        </ol>

        <h3>القسم 4: الإنهاء بسبب عدم الحضور</h3>
        <ol>
          <li>
            <strong>نسب الحضور المطلوبة:</strong>
            <ol type="a">
              <li>يجب على العضو حضور ما لا يقل عن 50٪ من الاجتماعات الإقليمية الدورية في كل نصف سنة.</li>
              <li>يجب أن يحضر العضو 30٪ على الأقل من الاجتماعات الدورية الإقليمية في كل نصف سنة.</li>
              <li>في حال عدم استيفاء هذه المتطلبات، يمكن إنهاء العضوية ما لم يوافق المجلس التنفيذي على العذر المقدم.</li>
            </ol>
          </li>
          <li>
            <strong>الغياب المتكرر:</strong>
            <ol type="a">
              <li>إذا تغيب أحد الأعضاء عن أربعة اجتماعات متتالية دون عذر مقبول أو موافقة مسبقة من المجلس، يتم إخطاره بأن غيابه قد يعتبر طلبًا غير مباشر لإنهاء العضوية.</li>
              <li>بعد ذلك، يجوز للمجلس، بأغلبية الأصوات، اتخاذ قرار بإنهاء عضويته رسميًا.</li>
            </ol>
          </li>
        </ol>

        <h3>القسم 5: الإنهاء لأسباب أخرى</h3>
        <ol>
          <li>
            <strong>الأسباب المبررة:</strong>
            <ol type="a">
              <li>يجوز للمجلس إنهاء عضوية أي عضو لم يعد مستوفيًا لمتطلبات العضوية أو لأسباب أخرى مشروعة، بشرط الحصول على موافقة ثلثي الأعضاء الإقليميين خلال اجتماع مخصص لهذا الغرض.</li>
              <li>تستند القرارات في هذا الاجتماع إلى المبادئ التوجيهية التالية:
                <ol type="i">
                  <li>المادة 8، القسم 1.</li>
                  <li>اختبار القيم الرباعي.</li>
                  <li>المعايير الأخلاقية العالية التي يتوجب على أعضاء الرابطة الالتزام بها.</li>
                </ol>
              </li>
            </ol>
          </li>
          <li>
            <strong>الإخطار:</strong>
            <ol type="a">
              <li>قبل اتخاذ أي إجراء، يجب إخطار العضو كتابيًا قبل 10 أيام على الأقل من اتخاذ القرار، ومنحه الفرصة للتقدم برد كتابي إلى المجلس.</li>
              <li>يحق للعضو المثول أمام المجلس للدفاع عن موقفه.</li>
              <li>يتم إرسال الإخطار شخصيًا أو عبر البريد المسجل إلى آخر عنوان مسجل للعضو.</li>
            </ol>
          </li>
          <li>
            <strong>ملء التصنيف بعد الإنهاء:</strong>
            <ol type="a">
              <li>بعد إنهاء عضوية أحد الأعضاء، لا يجوز للرابطة انتخاب عضو جديد في نفس التصنيف حتى انتهاء فترة الطعن وإصدار القرار النهائي من قبل الرابطة أو لجنة التحكيم.</li>
              <li>لا ينطبق هذا القيد إذا كان انتخاب العضو الجديد لا يؤثر على نسبة التوزيع المنصوص عليها حتى في حال إلغاء قرار الإنهاء لاحقًا.</li>
            </ol>
          </li>
        </ol>

        <h3>القسم 6: حق الطعن أو الوساطة أو التحكيم في قرار الإنهاء</h3>
        <ol>
          <li>
            <strong>الإخطار بالطعن:</strong>
            <ol type="a">
              <li>خلال 7 أيام من تاريخ قرار المجلس بإنهاء العضوية، يقوم أمين السر بإرسال إشعار كتابي بالقرار إلى العضو.</li>
              <li>خلال 14 يومًا من تاريخ الإخطار، يجوز للعضو تقديم طلب طعن رسمي للرابطة، أو طلب الوساطة أو التحكيم وفقًا لما هو منصوص عليه في النظام الأساسي.</li>
            </ol>
          </li>
          <li>
            <strong>موعد جلسة الاستئناف:</strong>
            <ol type="a">
              <li>في حال تقديم الطعن، يقوم المجلس بتحديد موعد لجلسة الاستماع خلال اجتماع منتظم للرابطة في غضون 21 يومًا من تاريخ طلب الطعن.</li>
                <li>يتم إرسال إخطار كتابي قبل 5 أيام على الأقل لجميع الأعضاء، موضحًا فيه جدول أعمال الاجتماع.</li>   
                <li>يسمح بحضور الأعضاء فقط أثناء جلسة الاستئناف</li> 
            </ol>

            <strong>الوساطة أو التحكيم:</strong>

            <ol type="a">
              <li>يتم تطبيق إجراءات الوساطة أو التحكيم وفقًا لما هو منصوص عليه في النظام الأساسي.</li>
            </ol>
            <strong>نتيجة الطعن:</strong>

            <ol type="a">
              <li>إذا تم تقديم الطعن، يكون قرار الرابطة نهائيًا وملزمًا لجميع الأطراف، ولا يجوز إخضاعه للتحكيم.</li>
            </ol>
            <strong>قرار المحكمين أو المجلس المختار:</strong>

            <ol type="a">
              <li>إذا طُلب التحكيم، يكون القرار الصادر عن المحكمين، أو في حال الخلاف، عن المجلس المختار، نهائيًا وملزمًا لجميع الأطراف، ولا يجوز الطعن فيه.</li>
            </ol>
            <strong>الوساطة غير الناجحة:</strong>

            <ol type="a">
              <li>إذا لم تنجح الوساطة، يجوز للعضو تقديم طعن رسمي للرابطة أو طلب التحكيم كما هو منصوص عليه أعلاه.</li>
            </ol>

         

         
          </ol>
